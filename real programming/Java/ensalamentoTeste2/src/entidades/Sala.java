package entidades;

public class Sala {
	
	// Atributos
	private String nome;

	// Construtores
	public Sala(String nome) {
		super();
		this.nome = nome;
	}
	
	// Métodos
	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}
	
}
